interface PruneArgs {
  sandbox: SandboxManager;
  sandboxId: string;
  benignSource: string;
  benignTestPath: string;
  originalProjectRoot: string;
  contractName: string;
  forkBlockNumber: number;
  rpcUrl: string;
}

interface PruneResult {
  survivors: string[];
  pruned: string[];
  prunedSource: string;
  lastRawOutput: string;
}

async function pruneAgainstUnpatched(args: PruneArgs): Promise<PruneResult> {
  // Write the generated source into the original project's test/ dir.
  const fullBenignPath = joinUnix(
    args.originalProjectRoot,
    args.benignTestPath
  );
  await args.sandbox.writeFile(
    args.sandboxId,
    fullBenignPath,
    args.benignSource
  );

  // Build the original so we can pull runtime bytecode for etching.
  const build = await args.sandbox.exec(
    args.sandboxId,
    `cd '${args.originalProjectRoot}' && forge build --extra-output storageLayout 2>&1`,
    180_000
  );
  if (build.exitCode !== 0) {
    return {
      survivors: [],
      pruned: [],
      prunedSource: args.benignSource,
      lastRawOutput: (build.stdout + "\n" + build.stderr).slice(-3_000),
    };
  }

  const artifactPath = `${args.originalProjectRoot}/out/${args.contractName}.sol/${args.contractName}.json`;
  const artifact = await args.sandbox.tryReadFile(args.sandboxId, artifactPath);
  if (!artifact) {
    return {
      survivors: [],
      pruned: [],
      prunedSource: args.benignSource,
      lastRawOutput: `Artifact not found: ${artifactPath}`,
    };
  }
  let runtimeBytecode = "";
  try {
    const parsed: any = JSON.parse(artifact);
    runtimeBytecode = parsed?.deployedBytecode?.object ?? "";
    if (runtimeBytecode && !runtimeBytecode.startsWith("0x")) {
      runtimeBytecode = `0x${runtimeBytecode}`;
    }
  } catch (e) {
    return {
      survivors: [],
      pruned: [],
      prunedSource: args.benignSource,
      lastRawOutput: `Artifact parse error: ${(e as Error).message}`,
    };
  }

  const envPrefix = [
    `ETH_RPC_URL='${args.rpcUrl.replace(/'/g, "'\\''")}'`,
    `DEXIBLE_FORK_BLOCK='${args.forkBlockNumber}'`,
    `DEXIBLE_RUNTIME_BYTECODE='${runtimeBytecode}'`,
    // Deliberately omit DEXIBLE_PATCHED so isPatched=false in the benign tests.
  ].join(" ");

  const cmd = `cd '${args.originalProjectRoot}' && ${envPrefix} forge test --match-path '${args.benignTestPath}' -vv --json 2>/tmp/forge.err; echo "__exit:$?"`;
  const run = await args.sandbox.exec(args.sandboxId, cmd, 600_000);
  const rawOutput = run.stdout + "\n----stderr----\n" + run.stderr;

  const parsedForge = tryParseForgeJson(run.stdout);
  const survivors: string[] = [];
  const pruned: string[] = [];
  if (parsedForge) {
    for (const contractKey of Object.keys(parsedForge)) {
      const contract: any = parsedForge[contractKey];
      if (!contract?.test_results) continue;
      for (const [name, tr] of Object.entries<any>(contract.test_results)) {
        const shortName = name.replace(/\(.*\)$/, "");
        if (tr?.status === "Success") survivors.push(shortName);
        else pruned.push(shortName);
      }
    }
  }

  // If at least one test survived, rewrite the source to contain ONLY
  // surviving tests so Blue doesn't have to dodge red herrings.
  let prunedSource = args.benignSource;
  if (pruned.length > 0 && survivors.length > 0) {
    prunedSource = stripFailingTests(args.benignSource, pruned);
    // Re-write the pruned source back so subsequent runs see it.
    await args.sandbox.writeFile(
      args.sandboxId,
      fullBenignPath,
      prunedSource
    );
  }

  return {
    survivors,
    pruned,
    prunedSource,
    lastRawOutput: rawOutput,
  };
}

// Remove `function <testName>(...) {...}` blocks whose base name is in the
// failing set. Keeps the rest of the file intact.
function stripFailingTests(source: string, failingNames: string[]): string {
  let result = source;
  for (const name of failingNames) {
    result = removeFunctionBlock(result, name);
  }
  return result;
}

function removeFunctionBlock(source: string, funcName: string): string {
  const startRe = new RegExp(
    `(\\n\\s*/\\*\\*[\\s\\S]*?\\*/)?\\s*function\\s+${escapeRegex(funcName)}\\s*\\(`,
    "m"
  );
  const match = source.match(startRe);
  if (!match) return source;
  const start = match.index ?? 0;
  // Find the opening brace of the function body.
  const braceIdx = source.indexOf("{", start);
  if (braceIdx < 0) return source;
  let depth = 1;
  let i = braceIdx + 1;
  while (i < source.length && depth > 0) {
    const ch = source[i];
    if (ch === "{") depth++;
    else if (ch === "}") depth--;
    i++;
  }
  if (depth !== 0) return source;
  return source.slice(0, start) + source.slice(i);
}

function escapeRegex(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function tryParseForgeJson(stdout: string): Record<string, unknown> | null {
  const start = stdout.indexOf("{");
  if (start < 0) return null;
  try {
    return JSON.parse(stdout.slice(start)) as Record<string, unknown>;
  } catch {
    const end = stdout.lastIndexOf("}");
    if (end <= start) return null;
    try {
      return JSON.parse(stdout.slice(start, end + 1)) as Record<string, unknown>;
    } catch {
      return null;
    }
  }
}

function joinUnix(...parts: string[]): string {
  return parts
    .map((p) => p.replace(/\\/g, "/").replace(/\/+$/, ""))
    .join("/")
    .replace(/\/+/g, "/");
}
