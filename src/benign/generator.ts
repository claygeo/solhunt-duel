// Benign test-suite generator. Produces a `test/Benign.t.sol` automatically
// from the target source + Red's exploit report, then prunes tests that don't
// pass on the UNPATCHED bytecode.
//
// Contract with Blue:
//   - Generated tests MUST pass on the original (unpatched) fork. If they
//     don't, they're wrong benign fixtures, not regressions, and we drop
//     them before handing the suite to Blue.
//   - Blue will then require them to also pass on the PATCHED bytecode.
//     Any test that passes on unpatched but fails on patched is a
//     *real* regression and the patch must be rejected.
//
// Minimum contract: at least 3 surviving tests. Fewer means we don't have a
// credible regression harness and Blue can't run.

import type { ExploitReport } from "../reporter/format.js";
import { SandboxManager } from "../sandbox/manager.js";
import {
  chatCompletion,
  type ProviderConfig,
  type Message,
} from "../agent/provider.js";
import {
  subscriptionCompletion,
  mapToClaudeCliModel,
} from "../agent/provider-subscription.js";

export interface GenerateBenignArgs {
  sandbox: SandboxManager;
  /** Sandbox container id that already has the ORIGINAL project seeded. */
  sandboxId: string;
  originalSource: string;
  /** Name of the .sol file relative to project src/. */
  sourceFilename: string;
  exploitReport: ExploitReport;
  /** Project root in-container that builds the UNPATCHED contract. */
  originalProjectRoot: string;
  /** Path under each project where the benign suite should live. */
  benignTestPath: string;
  /** Fork block and RPC for the forge run. */
  forkBlockNumber: number;
  rpcUrl: string;
  /** Target contract address on the fork. */
  targetAddress: string;
  /** Contract name for Forge to build + extract. */
  contractName: string;
  /** Minimum tests that must survive pruning. */
  minTests?: number;
  /** Model provider override. If omitted, reads env. */
  provider?: ProviderConfig;
}

export interface GenerateBenignResult {
  /** Final compilable test/Benign.t.sol contents. */
  benignSource: string;
  /** Test names that survived (passed on unpatched). */
  survivingTests: string[];
  /** Test names generated by the model that were pruned (failed on unpatched). */
  prunedTests: string[];
  attempts: number;
}

const DEFAULT_MIN_TESTS = 3;

export async function generateBenign(
  args: GenerateBenignArgs
): Promise<GenerateBenignResult> {
  const minTests = args.minTests ?? DEFAULT_MIN_TESTS;

  // Two attempts max. Second attempt gets a sharpened prompt.
  let lastError = "";
  let generated = "";
  let survivors: string[] = [];
  let pruned: string[] = [];

  for (let attempt = 1; attempt <= 2; attempt++) {
    const sharpen = attempt === 2 ? lastError : undefined;
    try {
      generated = await llmGenerateBenign({
        originalSource: args.originalSource,
        exploitReport: args.exploitReport,
        contractName: args.contractName,
        targetAddress: args.targetAddress,
        sharpenHint: sharpen,
        provider: args.provider,
      });
    } catch (err: any) {
      lastError = `LLM generation failed: ${err?.message ?? err}`;
      continue;
    }

    // Prune tests that fail on the unpatched bytecode.
    const pruneResult = await pruneAgainstUnpatched({
      sandbox: args.sandbox,
      sandboxId: args.sandboxId,
      benignSource: generated,
      benignTestPath: args.benignTestPath,
      originalProjectRoot: args.originalProjectRoot,
      contractName: args.contractName,
      forkBlockNumber: args.forkBlockNumber,
      rpcUrl: args.rpcUrl,
    });

    survivors = pruneResult.survivors;
    pruned = pruneResult.pruned;
    generated = pruneResult.prunedSource;

    if (survivors.length >= minTests) {
      return {
        benignSource: generated,
        survivingTests: survivors,
        prunedTests: pruned,
        attempts: attempt,
      };
    }

    lastError = `Only ${survivors.length}/${minTests} tests survived pruning on the unpatched fork. Pruned: ${pruned.join(", ")}. Last forge output: ${pruneResult.lastRawOutput.slice(0, 1_500)}`;
  }

  throw new Error(
    `Benign suite generator failed after 2 attempts: ${lastError}\n\n` +
      `Blue cannot run without a credible regression harness. ` +
      `Last generated source (first 1KB):\n${generated.slice(0, 1_000)}`
  );
}

// ---------------------------------------------------------------------
// LLM call
// ---------------------------------------------------------------------

async function llmGenerateBenign(args: {
  originalSource: string;
  exploitReport: ExploitReport;
  contractName: string;
  targetAddress: string;
  sharpenHint?: string;
  provider?: ProviderConfig;
}): Promise<string> {
  const vuln = args.exploitReport.vulnerability;
  const sharpenSection = args.sharpenHint
    ? `\n\n## First attempt was rejected\n\nHere's what happened on attempt 1:\n\n${args.sharpenHint}\n\nMake the tests more robust: rely on zero-default state, avoid calling functions that may not exist on the original bytecode, and skip assertions about specific return values unless you can derive them from the source.\n`
    : "";

  const systemPrompt = `You generate Foundry benign happy-path tests for smart-contract vulnerability harnesses.

Your output will be compiled against the ORIGINAL (unpatched) contract, etched at a real mainnet address on a mainnet fork. Tests that pass here define the regression baseline that a later patch must preserve.

Rules:
- Output ONLY compilable Solidity — a single contract named \`BenignTest is Test\`, plus any fixture contracts (tokens, mock routers) it needs. No markdown fences, no explanation.
- Solidity pragma: \`pragma solidity ^0.8.17;\`.
- Import \`forge-std/Test.sol\` for the cheatcodes.
- Do NOT \`import\` from \`src/\` — define minimal interfaces inline, same way Red does in the exploit test.
- Use \`vm.envString("ETH_RPC_URL")\`, \`vm.envOr("DEXIBLE_FORK_BLOCK", uint256(<defaultBlock>))\`, and \`vm.envBytes("DEXIBLE_RUNTIME_BYTECODE")\` with \`vm.etch(TARGET, code)\` in setUp so the harness's bytecode swap works. The target address goes in a \`constant TARGET\` at the top of the test contract.
- Read \`vm.envOr("DEXIBLE_PATCHED", uint256(0)) == 1\` into an \`isPatched\` bool so the same file works on both variants. Tests that call patched-only selectors must early-return when \`!isPatched\`.
- Use cheatcodes: \`makeAddr\`, \`deal\`, \`prank\`, \`vm.expectRevert\` as needed.
- Prefer tests that assert ONLY that a call does not revert, counts incremented, or simple state changes. Don't assert exact return values you can't derive from the source.
- 5-8 tests total. Every test name starts with \`test\` (Foundry convention).

Target the vulnerable functions listed below — generate happy-path tests that exercise those same functions with benign inputs, so a later patch is forced to preserve legitimate use.`;

  const userPrompt = `## Target contract

**Name:** ${args.contractName}
**Address on fork:** ${args.targetAddress}
**Block:** ${args.exploitReport.blockNumber}

## Vulnerability (for context — do NOT exploit it in the benign tests)

- Class: ${vuln.class}
- Affected functions: ${vuln.functions.join(", ") || "(none specified)"}
- Description: ${vuln.description}

## Contract source

\`\`\`solidity
${args.originalSource.slice(0, 35_000)}
\`\`\`${sharpenSection}

## Output

Return ONLY the complete \`test/Benign.t.sol\` file contents. No markdown fences, no prose.`;

  const messages: Message[] = [
    { role: "system", content: systemPrompt },
    { role: "user", content: userPrompt },
  ];

  // Route: subscription (claude -p) if any of these are set:
  //   - BENIGN_VIA_CLAUDE_CLI=1  (explicit, highest priority)
  //   - BLUE_VIA_CLAUDE_CLI=1    (default for subscription-mode duels)
  //   - BLUE_VIA_SUBSCRIPTION=1  (legacy flag, kept for back-compat)
  // Otherwise the configured OpenRouter provider.
  const benignFlag = process.env.BENIGN_VIA_CLAUDE_CLI;
  const blueCliFlag = process.env.BLUE_VIA_CLAUDE_CLI;
  const legacyFlag = process.env.BLUE_VIA_SUBSCRIPTION;
  const viaSubscription =
    benignFlag === "1" ||
    benignFlag === "true" ||
    blueCliFlag === "1" ||
    blueCliFlag === "true" ||
    legacyFlag === "1" ||
    legacyFlag === "true";

  let text: string;
  if (viaSubscription) {
    // Use Opus 4.7 by default on the subscription path — consistent with
    // Blue's brain in runBlueTeamViaClaudeCli, and Sonnet's Solidity output
    // is noticeably weaker. Override via BENIGN_MODEL / STRATEGIC_MODEL.
    const modelAlias =
      process.env.BENIGN_MODEL ??
      process.env.STRATEGIC_MODEL ??
      "claude-opus-4-7";
    const model = mapToClaudeCliModel(modelAlias);
    // 5-minute timeout — Solidity test generation on a 35KB contract
    // routinely takes 2-3 minutes on Opus.
    const resp = await subscriptionCompletion(
      { model, timeoutMs: 300_000 },
      messages
    );
    text = resp.message.content ?? "";
  } else {
    const provider = args.provider ?? resolveDefaultProvider();
    const resp = await chatCompletion(provider, messages);
    text = resp.message.content ?? "";
  }

  return extractSolidity(text);
}

function resolveDefaultProvider(): ProviderConfig {
  const key = process.env.OPENROUTER_API_KEY;
  if (!key) {
    throw new Error(
      "No provider configured for benign generator. Set OPENROUTER_API_KEY " +
        "or pass `provider` explicitly, or set BLUE_VIA_SUBSCRIPTION=1 to use claude -p."
    );
  }
  return {
    provider: "openrouter",
    model: process.env.BLUE_BENIGN_MODEL ?? "anthropic/claude-sonnet-4-6",
    baseUrl: "https://openrouter.ai/api/v1",
    apiKey: key,
  };
}

// Extract Solidity source from an LLM response. Opus sometimes wraps the
// output in markdown fences and prepends explanatory prose even when told
// not to. Strategy:
//   1. If there's a ```solidity / ```sol / ``` fenced block, take the LAST
//      one (Opus usually puts the final answer last).
//   2. Else, find the first `// SPDX` or `pragma solidity` and slice from
//      there — drops any preamble.
//   3. Else, return the trimmed raw text.
function extractSolidity(text: string): string {
  if (!text) return "";

  // 1. Prefer the last fenced code block.
  const fenceRe = /```(?:solidity|sol)?\s*\n([\s\S]*?)```/gi;
  let lastFence: string | null = null;
  let m: RegExpExecArray | null;
  while ((m = fenceRe.exec(text)) !== null) {
    if (m[1]) lastFence = m[1];
  }
  if (lastFence) return stripPreamble(lastFence.trim());

  // 2. Slice from first SPDX/pragma marker.
  return stripPreamble(text.trim());
}

function stripPreamble(src: string): string {
  const spdxIdx = src.indexOf("// SPDX-License-Identifier");
  const pragmaIdx = src.indexOf("pragma solidity");
  const candidates = [spdxIdx, pragmaIdx].filter((i) => i >= 0);
  if (candidates.length === 0) return src;
  const cut = Math.min(...candidates);
  return cut > 0 ? src.slice(cut).trim() : src;
}

